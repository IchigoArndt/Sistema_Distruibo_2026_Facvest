class Professional {
  final int id;
  final String name;
  final String specialty;
  final double rating;
  final int reviews;
  final String phone;
  final String email;
  final String methodology;
  final double price;
  final String experience;
  final String about;
  final List<String> certifications;

  Professional({
    required this.id,
    required this.name,
    required this.specialty,
    required this.rating,
    required this.reviews,
    required this.phone,
    required this.email,
    required this.methodology,
    required this.price,
    required this.experience,
    required this.about,
    required this.certifications,
  });
}
